<p align="center" class="style8 style9"><u>Master of Business Administration</u></p>
<p>The MBA program  is approved by All India Council of Technical Education (A.I.C.T.E.),New Delhi  and Directorate of Technical Education(D.T.E.), M.P. and Dept. of Higher  Education , Govt. of M.P. and is affiliated to Jiwaji University, Gwalior.</p>
<p><br />
    <span class="style15"><strong>Specialization </strong></span><br />
  The MBA course  offers three specialization viz., Marketing, Finance &amp; HR. The student can  opt for one core specialization.</p>
<p><br />
  <span class="style15"><strong>Admission procedure </strong></span><br />
  Admissions  are through on-line/ off-line campus counselling conducted by DTE / MP Online,  Bhopal.</p>
<p><br />
  <strong class="style15">Intake </strong><br />
  180 seats  (Reservation SC-16%, ST-20%, OBC-14%)</p>
<p><br />
  <strong class="style15">Duration</strong><br />
  Two Year  (Full Time) consisting of four semesters.</p>
<p><br />
  <strong class="style15">Eligibility</strong><br />
  Graduation  in any discipline with at least 50% marks (at least 45% marks in case of SC,  ST, and OBC-excluding creamy layer).<br /></p>

